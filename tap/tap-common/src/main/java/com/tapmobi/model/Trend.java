package com.tapmobi.model;

public class Trend {
	private String date;
	private String hour;
	private float trafficTrend;
	private float installTrend;
	private String region;
	private String appId;
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the hour
	 */
	public String getHour() {
		return hour;
	}
	/**
	 * @param hour the hour to set
	 */
	public void setHour(String hour) {
		this.hour = hour;
	}
	/**
	 * @return the trafficTrend
	 */
	public float getTrafficTrend() {
		return trafficTrend;
	}
	/**
	 * @param trafficTrend the trafficTrend to set
	 */
	public void setTrafficTrend(float trafficTrend) {
		this.trafficTrend = trafficTrend;
	}
	/**
	 * @return the installTrend
	 */
	public float getInstallTrend() {
		return installTrend;
	}
	/**
	 * @param installTrend the installTrend to set
	 */
	public void setInstallTrend(float installTrend) {
		this.installTrend = installTrend;
	}
	/**
	 * @return the region
	 */
	public String getRegion() {
		return region;
	}
	/**
	 * @param region the region to set
	 */
	public void setRegion(String region) {
		this.region = region;
	}
	/**
	 * @return the appId
	 */
	public String getAppId() {
		return appId;
	}
	/**
	 * @param appId the appId to set
	 */
	public void setAppId(String appId) {
		this.appId = appId;
	}


}
